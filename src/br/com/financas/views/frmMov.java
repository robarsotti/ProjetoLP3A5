    public void listarTable(){
       MovimentacaoDAO dao = new MovimentacaoDAO();
        List<Movimentacao> lista = dao.listarMovimentacao();
        DefaultTableModel dados = (DefaultTableModel) tblMovimentacao.getModel();
        dados.setNumRows(0);
        
        for(Movimentacao c: lista){
            
            
            Categorias categoriaAtual = categorias.stream().filter(categoria -> categoria.getId()==c.getIdCat()).findFirst().get();
            
            TipoMovimentacao tipoMovAtual = mov.stream().filter(movimentacao -> movimentacao.getId()==c.getIdTipo()).findFirst().get();
            
            
            dados.addRow(new Object[]{
            c.getId(),
            c.getValor(),
            c.getDtMov(),
            c.getDtCadastro(),
            tipoMovAtual.getTipo(),
            categoriaAtual.getDescricao()
            });
          
        }
        double credito = this.somarValoresMovimentacao(lista,1);
        
        double debito = this.somarValoresMovimentacao(lista,2);
        
        
        
        txtSaldo.setText(String.valueOf(credito - debito));
               
        
        
    }
    
    private double somarValoresMovimentacao(List<Movimentacao> movimentacoes, int idTipo){
        return movimentacoes
                        .stream()
                        .filter(movimentacao -> movimentacao.getIdTipo()==idTipo)
                        .mapToDouble(movimentacao -> movimentacao.getValor())
                        .sum(); 
    
    }
