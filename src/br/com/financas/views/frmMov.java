    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        // TODO add your handling code here:
        
            Movimentacao obj = new Movimentacao();
            obj.setValor(Double.parseDouble(txtValor.getText()));
            obj.setDtMov(txtData.getText());
            Optional<Categorias> categoriasSelecionada = categorias.stream().filter(categoria -> categoria.getDescricao().equals(cboCat.getSelectedItem().toString())).findFirst();
            obj.setIdCat(categoriasSelecionada.get().getId());
            
            Optional<TipoMovimentacao> tipomovselecionadas = mov.stream().filter(movimentacao -> movimentacao.getTipo().equals(cboTipo.getSelectedItem().toString())).findFirst();
            obj.setIdTipo(tipomovselecionadas.get().getId());
            
            
            
            MovimentacaoDAO dao = new MovimentacaoDAO();
            dao.cadastrarMovimentacao(obj);
            jTabbedPane1.setSelectedIndex(0);
            
            
        
    }//GEN-LAST:event_btnCadastrarActionPerformed

