    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        // TODO add your handling code here:
        
        //carrega lista de categorias
        
        CategoriasDAO cate = new CategoriasDAO();
        categorias = cate.listarCategorias();
        categorias.stream().forEach(categoria -> {
        cboCat.addItem(categoria.getDescricao());
        });
        
        TipoMovimentacaoDAO tpdao = new TipoMovimentacaoDAO();
        mov = tpdao.listarTipoMov();
        mov.stream().forEach(movimentacao -> {
        cboTipo.addItem(movimentacao.getTipo());
        });
        
        
        listarTable();
        
        
        
    }//GEN-LAST:event_formWindowActivated

